// ============================================================================
// DataTypesMessage - Data type negotiation (TNS_MSG_TYPE_DATA_TYPES)
// ============================================================================

/// Data types negotiation message.
///
/// Sent to negotiate supported data types and capabilities with the Oracle server.
/// This message contains compile-time and runtime capabilities, character set info,
/// and the complete list of supported data types.
pub struct DataTypesMessage<'a> {
    /// Compile-time capabilities
    pub compile_caps: &'a [u8],
    /// Runtime capabilities
    pub runtime_caps: &'a [u8],
}

impl Message for DataTypesMessage<'_> {
    fn wire_size(&self) -> usize {
        let mut size = 0;
        size += 1; // message type (TNS_MSG_TYPE_DATA_TYPES)
        size += 2; // charset (u16 LE)
        size += 2; // ncharset (u16 LE)
        size += 1; // encoding flags
        size += bytes_with_length_wire_size(self.compile_caps.len());
        size += bytes_with_length_wire_size(self.runtime_caps.len());
        size += data_types_array_wire_size();
        size
    }

    fn write_to(&self, buf: &mut Vec<u8>) -> Result<()> {
        buf.write_u8(TNS_MSG_TYPE_DATA_TYPES);
        buf.write_u16_le(TNS_CHARSET_UTF8);
        buf.write_u16_le(TNS_CHARSET_UTF8);
        buf.write_u8(TNS_ENCODING_MULTI_BYTE | TNS_ENCODING_CONV_LENGTH);
        buf.write_bytes_with_length(self.compile_caps);
        buf.write_bytes_with_length(self.runtime_caps);
        write_data_types_array(buf);
        Ok(())
    }
}

impl DataMessage for DataTypesMessage<'_> {}

// ============================================================================
// Data Types Array Helpers
// ============================================================================

/// Calculate the wire size of the data types array.
/// Each entry is 8 bytes (4 x u16), plus 2 bytes for the terminator.
pub const fn data_types_array_wire_size() -> usize {
    DATA_TYPES.len() * 8 + 2
}

/// Write the data types array to a Vec<u8>.
/// Format: data_type (u16 BE), conv_type (u16 BE), representation (u16 BE), unused (u16 BE)
pub fn write_data_types_array(buf: &mut Vec<u8>) {
    for (dt, conv, rep) in DATA_TYPES {
        buf.extend_from_slice(&dt.to_be_bytes());
        buf.extend_from_slice(&conv.to_be_bytes());
        buf.extend_from_slice(&rep.to_be_bytes());
        buf.extend_from_slice(&0u16.to_be_bytes());
    }
    // End of data types terminator
    buf.extend_from_slice(&0u16.to_be_bytes());
}

/// Write all data types to a WriteBuffer in the TNS format.
/// Format: data_type (u16 BE), conv_type (u16 BE), representation (u16 BE), unused (u16 BE)
pub fn write_data_types_to_buffer(buf: &mut WriteBuffer) {
    for (dt, conv, rep) in DATA_TYPES {
        buf.write_u16_be(*dt);
        buf.write_u16_be(*conv);
        buf.write_u16_be(*rep);
        buf.write_u16_be(0);
    }
    // End of data types terminator
    buf.write_u16_be(0);
}


#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_data_types_message_wire_size() {
        let compile_caps = vec![0u8; 64];
        let runtime_caps = vec![0u8; 16];

        let msg = DataTypesMessage {
            compile_caps: &compile_caps,
            runtime_caps: &runtime_caps,
        };

        let mut buf = Vec::with_capacity(msg.wire_size());
        msg.write_to(&mut buf).unwrap();

        assert_eq!(buf.len(), msg.wire_size());
    }

    #[test]
    fn test_data_types_array_size() {
        // 319 entries * 8 bytes + 2 byte terminator = 2554 bytes
        assert_eq!(data_types_array_wire_size(), 319 * 8 + 2);
    }
}
